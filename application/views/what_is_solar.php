<h1> What is Solar (Photovoltaics)?</h1>
<hr />

<p><a href="/assets/images/large/DSC00088.jpg" class="lightbox"><img class="floatright" src="/assets/images/medium/DSC00088.jpg" width="350" height="263" alt="Stockbridge Regional Solar Installation Provider" /></a> Solar energy is energy that comes from the sun. Solar Power Plants are comprised of Photovoltaic modules, usually rectangular shaped, that convert sunlight directly into electricity. The modules create DC (Direct Current) electricity in which electrons flow in one direction only. The DC current is converted into AC (Alternating Current) by an Inverter. There are several types of photovoltaic technologies Stockbridge Electric specializes in the following: Crystalline, Polycrystalline and Thin Film. </p>
<ol>
  <li> Crystalline solar cells are made from a single crystal of silicon. </li>
  <li> Polycrystalline solar cells are fabricated from multiple crystals of silicon. </li>
  <li>Thin Film Solar is a layer of semiconductor material such as copper indium diselenide, cadmium telluride, gallium arsenide, or amorphous silicon, a few microns or less in thickness, used to make photovoltaic cells.</li>
</ol>
